const DICTS: Record<Lang, Dict> = { fr: FR, en: EN };

interface I18nCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
}

const Ctx = createContext<I18nCtx>({ lang: 'fr', setLang: () => {}, t: (k: string) => k });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = typeof localStorage !== 'undefined' ? (localStorage.getItem('lang') as Lang | null) : null;
    if (saved === 'fr' || saved === 'en') return saved;
    const nav = typeof navigator !== 'undefined' ? navigator.language : 'fr';
    return nav.startsWith('en') ? 'en' : 'fr';
  });
  const setLang = (l: Lang) => {
    setLangState(l);
    try {
      localStorage.setItem('lang', l);
    } catch {
      /* ignore */
    }
  };
  useEffect(() => {
    if (typeof document !== 'undefined') document.documentElement.lang = lang;
  }, [lang]);
  const t = (key: string, params?: Record<string, string | number>) => {
    let out = DICTS[lang][key] ?? DICTS.fr[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        out = out.split(`{${k}}`).join(String(v));
      }
    }
    return out;
  };
  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>;
}

export function useI18n() {
  return useContext(Ctx);
}
